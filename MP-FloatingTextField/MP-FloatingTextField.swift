import Foundation


public final class MPFloatingTextField {
    
    
    let name = "MP-FloatingTextField"
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }

}
